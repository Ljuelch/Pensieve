// Translation helper function for data
function td(key, lang = null) {
    const language = lang || window.currentLanguage || 'de';

    // Search in all categories
    const categories = ['talents', 'rotation', 'powerSpikes', 'classQuests', 'gear', 'professions', 'common'];

    for (const category of categories) {
        if (dataTranslations[language] && dataTranslations[language][category] && dataTranslations[language][category][key]) {
            return dataTranslations[language][category][key];
        }
    }

    // Return original if no translation found
    return key;
}